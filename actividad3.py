class MapNode:
    def __init__ (self, position,cost,parent=None):
        self.position = position
        self.cost = cost
        self.parent = parent
        
    def __eq__ (self, other):
        return self.position[0] == other.position[0] and self.position[1] == other.position[1]
    
class Dijkstra(object):
    def run(self, mapF, start, end):
        mapF = mapF.astype(np.float64)
        unique, counts = np.unique(mapF, return_counts = True)
        nodeOne = counts[1]
        pathF=[]
        vectorVisited = []
        vectorLabel = []
        mapRow , mapCol = np.shape(mapF)
        visited = np.zeros(mapF.shape)
        costs = np.zeros (mapF.shape)
        vectorLabel.append(MapNode(start[::-1],0))
        endNode = MapNode(end[::-1],0)
        
        while(len(vectorVisited)!=nodeOne):
            currentNode = vectorLabel.pop(0)
            movements =[
                [0,-1,1],
                [-1,0,1],
                [1,0,1],
                [0,1,1]]
            
            for move in movements:
                newPosition = [currentNode.position[0] + move[0], currentNode.position[1] + move[1]]
                adjacentNode = MapNode(newPosition,currentNode.cost+move[2],currentNode)
                if newPosition[0] < 0 or newPosition[1] < 0 or newPosition[1] >= mapCol or newPosition[0] >= mapRow:
                    continue
                elif mapF[newPosition[0]][newPosition[1]]==0:
                    continue
                elif visited [newPosition[0]][newPosition[1]]==1:
                    continue
                else:
                    finded = False
                    for labeled in vectorLabel:
                        if(labeled == adjacentNode):
                            finded = True
                            if labeled.cost > adjacentNode.cost:
                                labeled.cost = adjacentNode.cost
                                costs[newPosition[0]][newPosition[1]] =adjacentNode.cost
                                labeled.parent = currentNode
                                
                    if not finded:
                        vectorLabel.append(adjacentNode)
                        costs[newPosition[0]][newPosition[1]] = adjacentNode.cost
                        
            vectorVisited.append(currentNode)
            
            if (currentNode == endNode):
                break
            
            visited[currentNode.position[0]][currentNode.position[1]]=1
            vectorLabel = sorted(vectorLabel, key = lambda x: x.cost)
            
        for visitedNode in vectorVisited:
            
            if visitedNode == endNode:
                endNode = visitedNode
                break
            
        while endNode is not None:
            pathF.append(endNode.position)
            endNode = endNode.parent
            
        return pathF, visited, costs
            
                
